#Ejemplo de Operadores de Comparacion

m= int (input("ingrese el valor de M:"))
n = int (input("ingrese el valor de N:"))

print(m == n) #Igualdad
print(m > n) # mayor que >
print(m < n) # menor que <
print(m >= n) #mayor o igual que >=
print(m <= n) #menor o igual que <=
print(m != n) # No igual o diferente !